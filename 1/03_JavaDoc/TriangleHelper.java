/**
 * This class provides a method to compute the area of a triangle {@link Triangle}.
 * @author Bobby Chan
 * @version 1.0
 */
public class TriangleHelper {
    /**
     * This method computes the area of a triangle given the base and height.
     * <a href="https://www.sfu.ca">Simon Fraser University</a>
     * @param base length
     * @param height height
     * @return area of the triangle
     */
    public static double computeArea(double base, double height) {
        return 0.5 * base * height;
    }
}